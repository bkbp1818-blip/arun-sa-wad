async function main() {
  console.log("Seeding explore places...");

  // Check if already seeded
  const existing = await prisma.explorePlace.count();
  if (existing > 0) {
    console.log(`Already have ${existing} places. Skipping seed.`);
    return;
  }

  for (let i = 0; i < PLACES.length; i++) {
    const place = PLACES[i];
    await prisma.explorePlace.create({
      data: {
        ...place,
        sortOrder: i,
      },
    });
    console.log(`  Created: ${place.nameTh}`);
  }

  console.log(`Done! Seeded ${PLACES.length} explore places.`);
}

main()
  .catch((e) => {
    console.error(e);
    process.exit(1);
  })
  .finally(() => prisma.$disconnect());
